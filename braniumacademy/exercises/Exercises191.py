# Đọc dữ liệu đầu vào
aStr, bStr = input().split()
a = int(aStr) # Chuyển str sang kiểu int
b = int(bStr)
# In ra kết quả
print(f"{a} + {b} = {a + b}") # Ctrl D: nhân bản 1 dòng, để xóa 1 dòng Ctrl X
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")
print(f"{a} % {b} = {a % b}")
print(f"{a} ** {b} = {a ** b}")
print(f"{a} // {b} = {a // b}")
