import math

# Tìm ước chung lớn nhất, bội chung nhỏ nhất
print(f"Greatest common divisor of 3, 4, 5: {math.gcd(3, 4, 5)}")
print(f"Less common multiple of 3, 4, 5: {math.lcm(3, 4, 5)}")

# Tính n!
print(f'Calculate 10! = {math.factorial(10)}')

# Tính sin, cos của pi/3
print(f"sin(pi/3): {math.sin(math.pi / 3)}")
print(f"cos(pi/3): {math.cos(math.pi / 3)}")

# Tìm căn bậc 2 của n
print(f"Square root of 100: {math.sqrt(100)}")

# Chuyển từ radian <=> độ
print(f"pi/3 to degree: {math.degrees(math.pi / 3)}")
print(f"45 degree to radian: {math.radians(45)}")